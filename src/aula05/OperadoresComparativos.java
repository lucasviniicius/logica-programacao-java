package aula05;

public class OperadoresComparativos {
    public static void main(String[] args) {
        int pao = 10;
        int acucar = 15;

        System.out.println(pao == acucar); // false
        System.out.println(pao != acucar); // true
        System.out.println(pao > acucar); // false
        System.out.println(pao < acucar); // true
        System.out.println(pao >= acucar); // false
        System.out.println(pao <= acucar); // true
    }
}
